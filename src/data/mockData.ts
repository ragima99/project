export const generateOrders = (): Order[] => {
  return orderData
    .filter(data => data.weight_ton !== null)
    .map(data => ({
      id: uuidv4(),
      customerId: data.customer_id,
      items: `Package for Customer ${data.customer_id}`,
      weight: data.weight_ton * 1000,
      volume: (data.weight_ton * 0.8),
      status: 'pending' as const,
      createdAt: new Date().toISOString(),
      deliveryDate: null
    }));
};

